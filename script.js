/* ===========================================
   CUSTOM BACKGROUND PARTICLES (no library)
   =========================================== */
(function () {
  const container = document.getElementById('particles-js');
  if (!container) return;

  // create canvas inside the existing div
  const canvas = document.createElement('canvas');
  container.appendChild(canvas);
  const ctx = canvas.getContext('2d');

  let width, height;
  function resize() {
    const rect = container.getBoundingClientRect();
    width = rect.width;
    height = rect.height;
    canvas.width = width;
    canvas.height = height;
  }
  window.addEventListener('resize', resize);
  resize();

  const NUM_PARTICLES = 80;
  const MAX_LINE_DIST = 140;
  const particles = [];

  function rand(min, max) {
    return Math.random() * (max - min) + min;
  }

  // create particles
  for (let i = 0; i < NUM_PARTICLES; i++) {
    particles.push({
      x: rand(0, width),
      y: rand(0, height),
      r: rand(2, 4),
      vx: rand(-0.7, 0.7),
      vy: rand(-0.7, 0.7)
    });
  }

  // mouse repulse effect
  const mouse = { x: null, y: null, radius: 120 };

  window.addEventListener('mousemove', (e) => {
    const rect = container.getBoundingClientRect();
    mouse.x = e.clientX - rect.left;
    mouse.y = e.clientY - rect.top;
  });

  window.addEventListener('mouseleave', () => {
    mouse.x = null;
    mouse.y = null;
  });

  function update() {
    for (let p of particles) {
      p.x += p.vx;
      p.y += p.vy;

      // bounce off edges
      if (p.x < 0 || p.x > width) p.vx *= -1;
      if (p.y < 0 || p.y > height) p.vy *= -1;

      // simple mouse repulsion
      if (mouse.x !== null && mouse.y !== null) {
        const dx = p.x - mouse.x;
        const dy = p.y - mouse.y;
        const dist = Math.sqrt(dx * dx + dy * dy);
        if (dist < mouse.radius && dist > 0) {
          const force = (mouse.radius - dist) / mouse.radius;
          p.x += (dx / dist) * force * 3;
          p.y += (dy / dist) * force * 3;
        }
      }
    }
  }

  function draw() {
    ctx.clearRect(0, 0, width, height);

    // draw particles
    for (let p of particles) {
      ctx.beginPath();
      ctx.arc(p.x, p.y, p.r, 0, Math.PI * 2);
      ctx.fillStyle = 'rgba(0, 0, 0, 0.8)'; // dark dots
      ctx.fill();
    }

    // draw connecting lines
    for (let i = 0; i < NUM_PARTICLES; i++) {
      for (let j = i + 1; j < NUM_PARTICLES; j++) {
        const p1 = particles[i];
        const p2 = particles[j];
        const dx = p1.x - p2.x;
        const dy = p1.y - p2.y;
        const dist = Math.sqrt(dx * dx + dy * dy);
        if (dist < MAX_LINE_DIST) {
          const alpha = 1 - dist / MAX_LINE_DIST;
          ctx.strokeStyle = `rgba(0, 0, 0, ${alpha * 0.4})`;
          ctx.lineWidth = 1;
          ctx.beginPath();
          ctx.moveTo(p1.x, p1.y);
          ctx.lineTo(p2.x, p2.y);
          ctx.stroke();
        }
      }
    }
  }

  function animate() {
    update();
    draw();
    requestAnimationFrame(animate);
  }
  animate();
})();
